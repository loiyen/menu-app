    <script>
        document.addEventListener('DOMContentLoaded', function() {
            function setupLiveSearch(inputId, resultId, url) {
                const input = document.getElementById(inputId);
                const resultDiv = document.getElementById(resultId);
                let debounceTimeout = null;

                if (!input || !resultDiv) return;

                input.addEventListener('keyup', function() {
                    clearTimeout(debounceTimeout);

                    debounceTimeout = setTimeout(() => {
                        const keyword = input.value.trim();
                        if (keyword.length === 0) {
                            resultDiv.innerHTML = "";
                            return;
                        }

                        fetch(`${url}?keyword=${encodeURIComponent(keyword)}`)
                            .then(res => res.text())
                            .then(html => resultDiv.innerHTML = html)
                            .catch(err => {
                                console.error("Fetch error:", err);
                                resultDiv.innerHTML =
                                    `<div class="alert alert-danger">Error saat mencari.</div>`;
                            });
                    }, 300);
                });
            }

            setupLiveSearch('search-makanan', 'hasil-makanan', '/menu/search-live');
            setupLiveSearch('search-menu1', 'hasil-menu1', '/menu1/search-live');
        });
    </script>

